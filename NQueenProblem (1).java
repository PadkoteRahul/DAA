/*
Akbar used to play chess with Birbal almost every evening.
Once Akbar has challanged Birbal to place the queens in each row of the board,
such that no queen should attack another queen on the board.

You are given an integer N, indicates the size of the board as N*N.
Your task is to help Birbal to find and win in this challange, 
if there is a solution, print the answer as shown in the samples
Otherwise, print "No Solution"

Input Format:
-------------
An integer N, size of the chess board.

Output Format:
--------------
Print any possible solution.


Sample Input-1:
---------------
4

Sample Output-1:
----------------
0010
1000
0001
0100



Sample Input-2:
---------------
3

Sample Output-2:
----------------
No Solution

*/

/*
We cannot place any two queens in the same row thus every queen will 
be placed in different rows. Now, we can place ith queen to ith row if 
the square we are placing in, is safe to place due to the previously
placed queens; if there are no such squares left we can backtrack and return false. 

For every queen we are placing we have to check, 

There should be no queen in the same column,
There should be no queen in the same row,
There should be no queen in the same diagonal (upper left and right diagonal).

We can eliminate the row search as we are already moving row wise to place the
queens thus there will be no other queen in the same row.
*/


import java.util.*;

public class NQueenProblem {
	int N;
	void printBoard(int board[][])
	{
		for (int i = 0; i < N; i++) {
			for (int j = 0; j < N; j++)
				System.out.print(board[i][j]);
			System.out.println();
		}
	}

	boolean isValid(int chess[][], int row, int col)
	{
		System.out.println("isValid row " + row + " col " + col);
		// Check this column only upward 
		for (int r = 0; r < row; r++)
			if (chess[r][col] == 1)
				return false;

		// Check upper diagonal on right side 
		for (int r = row, c = col; c < N && r >= 0; r--, c++)
			if (chess[r][c] == 1)
				return false;

		// Check upper diagonal on left side 
		for (int r = row, c = col; r >= 0 && c >= 0; r--, c--)
			if (chess[r][c]==1)
				return false;

		return true;
	}

	boolean recurNqueen(int chess[][], int row)
	{
		// If no queens are left to place we return true
		if (row >= N)
			return true;

		for (int col = 0; col < N; col++) 
		{
			// Check if placing queen to chess[r][i] is valid.
			if (isValid(chess, row, col)) 
			{
				System.out.println("queen row " + row + " col " + col);
				// Place this queen in chess[r][i] 
				chess[row][col] = 1;
	 
				// move ahead for next row 
				if (recurNqueen(chess, row + 1))
					return true;
				
				System.out.println("Backtracking row " + row + " col " + col);
				// Backtracking 
				chess[row][col] = 0; 
			}
		}
		// If the queen cannot be placed in any column for the given row we return false.
		return false;
	}

	boolean solveNQ()
	{
		// All values are initialized with zeros
		int board[][] = new int[N][N];

		// Start with row 0
		if (recurNqueen(board, 0) == false) {
			System.out.print("No Solution");
			return false;
		}

		printBoard(board);
		return true;
	}

	public static void main(String args[])
	{
		Scanner sc=new Scanner(System.in);
		NQueenProblem Queen = new NQueenProblem();
		Queen.N = sc.nextInt();
		Queen.solveNQ();
	}
}


